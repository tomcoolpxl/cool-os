/* Helper: fast memmove using 64-bit copies where possible */
static void *memmove(void *dest, const void *src, uint64_t n) {
    uint8_t *d = (uint8_t *)dest;
    const uint8_t *s = (const uint8_t *)src;

    if (d < s) {
        /* Copy forward - use 64-bit words for bulk */
        uint64_t *d64 = (uint64_t *)d;
        const uint64_t *s64 = (const uint64_t *)s;
        uint64_t words = n / 8;
        uint64_t rem = n % 8;

        for (uint64_t i = 0; i < words; i++) {
            d64[i] = s64[i];
        }
        /* Copy remaining bytes */
        d += words * 8;
        s += words * 8;
        for (uint64_t i = 0; i < rem; i++) {
            d[i] = s[i];
        }
    } else {
        /* Copy backward */
        uint64_t words = n / 8;
        uint64_t rem = n % 8;

        /* Copy trailing bytes first */
        for (uint64_t i = 0; i < rem; i++) {
            d[n - 1 - i] = s[n - 1 - i];
        }
        /* Copy 64-bit words backward */
        uint64_t *d64 = (uint64_t *)d;
        const uint64_t *s64 = (const uint64_t *)s;
        for (uint64_t i = words; i > 0; i--) {
            d64[i - 1] = s64[i - 1];
        }
    }
    return dest;
}

/* Helper to print decimal number */
static void console_print_dec(uint32_t val) {
    char buf[12];
    int i = 11;
    buf[i] = '\0';
    if (val == 0) {
        serial_putc('0');
        return;
    }
    while (val > 0 && i > 0) {
        buf[--i] = '0' + (val % 10);
        val /= 10;
    }
    serial_puts(&buf[i]);
}

/* Draw a single character glyph at character position (cx, cy) */
static void console_draw_char(uint32_t cx, uint32_t cy, char c) {
    const framebuffer_t *fb = fb_get_info();
    if (fb == NULL) return;

    const uint8_t *glyph = font[(uint8_t)c];
    uint32_t px = cx * FONT_WIDTH;
    uint32_t py = cy * FONT_HEIGHT;

    /* Write directly to buffer for speed (avoid 128 function calls) */
    uint8_t *buffer = (uint8_t *)(fb->back ? fb->back : fb->front);
    uint32_t pitch = fb->back ? fb->back_pitch : fb->hw_pitch;

    for (uint32_t row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = glyph[row];
        uint32_t *row_ptr = (uint32_t *)(buffer + (py + row) * pitch);
        for (uint32_t col = 0; col < FONT_WIDTH; col++) {
            row_ptr[px + col] = (bits & (0x80 >> col)) ? fg_color : bg_color;
        }
    }
}

/* Scroll the console up by one text row */
static void console_scroll(void) {
    const framebuffer_t *fb = fb_get_info();
    if (fb == NULL) return;

    /* Use back buffer if available (fast RAM), otherwise front (slow VRAM) */
    uint8_t *buffer = (uint8_t *)(fb->back ? fb->back : fb->front);
    uint32_t pitch = fb->back ? fb->back_pitch : fb->hw_pitch;

    uint32_t scroll_bytes = FONT_HEIGHT * pitch;
    uint32_t total_height = rows * FONT_HEIGHT;

    /* Move rows up using memmove (handles overlapping regions) */
    memmove(buffer, buffer + scroll_bytes, (total_height - FONT_HEIGHT) * pitch);

    /* Clear bottom row - use 64-bit writes (2 pixels at a time) */
    uint64_t bg_pair = ((uint64_t)bg_color << 32) | bg_color;
    for (uint32_t y = (rows - 1) * FONT_HEIGHT; y < rows * FONT_HEIGHT; y++) {
        uint64_t *row_ptr = (uint64_t *)(buffer + y * pitch);
        uint32_t pairs = fb->render_width / 2;
        for (uint32_t x = 0; x < pairs; x++) {
            row_ptr[x] = bg_pair;
        }
    }
}

void console_init(void) {
    const framebuffer_t *fb = fb_get_info();
    if (fb == NULL) {
        serial_puts("console: Framebuffer not available\n");
        return;
    }

    /* Calculate grid dimensions */
    cols = fb->render_width / FONT_WIDTH;
    rows = fb->render_height / FONT_HEIGHT;

    /* Set colors: white on black */
    fg_color = 0x00FFFFFF;
    bg_color = 0x00000000;

    /* Reset cursor */
    cursor_x = 0;
    cursor_y = 0;

    /* Clear screen */
    fb_clear(bg_color);

    initialized = 1;

    serial_puts("console: Initialized ");
    console_print_dec(cols);
    serial_puts("x");
    console_print_dec(rows);
    serial_puts(" (");
    console_print_dec(fb->render_width);
    serial_puts("x");
    console_print_dec(fb->render_height);
    serial_puts(")\n");
}

void console_putc(char c) {
    if (!initialized) return;

    switch (c) {
        case '\n':
            /* Move to start of next line */
            cursor_x = 0;
            cursor_y++;
            if (cursor_y >= rows) {
                console_scroll();
                cursor_y = rows - 1;
            }
            break;

        case '\r':
            /* Move cursor to start of current line */
            cursor_x = 0;
            break;

        case '\t':
            /* Advance to next tab stop (every 4 columns) */
            cursor_x = (cursor_x + 4) & ~3;
            if (cursor_x >= cols) {
                cursor_x = 0;
                cursor_y++;
                if (cursor_y >= rows) {
                    console_scroll();
                    cursor_y = rows - 1;
                }
            }
            break;

        case '\b':
            /* Move cursor back one position (no erase) */
            if (cursor_x > 0) {
                cursor_x--;
            }
            break;

        default:
            /* Draw the character glyph */
            console_draw_char(cursor_x, cursor_y, c);
            cursor_x++;
            if (cursor_x >= cols) {
                cursor_x = 0;
                cursor_y++;
                if (cursor_y >= rows) {
                    console_scroll();
                    cursor_y = rows - 1;
                }
            }
            break;
    }
}

void console_puts(const char *s) {
    if (!initialized) return;
    while (*s) {
        console_putc(*s++);
    }
    fb_present();
}

void console_clear(void) {
    if (!initialized) return;
    fb_clear(bg_color);
    cursor_x = 0;
    cursor_y = 0;
    fb_present();
}

void console_erase_char(void) {
    if (!initialized) return;
    if (cursor_x > 0) {
        cursor_x--;
        console_draw_char(cursor_x, cursor_y, ' ');
        fb_present();
    }
}
